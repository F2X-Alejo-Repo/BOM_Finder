LAYER_VARIANTS_CSV = """\
Ref,PosX,PosY,Rot,Side
C1,0,0,0,top
C2,0,0,0,Top
C3,0,0,0,T
C4,0,0,0,F
C5,0,0,0,front
C6,0,0,0,F.Cu
R1,0,0,0,bottom
R2,0,0,0,Bottom
R3,0,0,0,B
R4,0,0,0,b.cu
"""


@pytest.mark.parametrize("idx,expected_layer", [
    (0, "Top"),
    (1, "Top"),
    (2, "Top"),
    (3, "Top"),
    (4, "Top"),
    (5, "Top"),
    (6, "Bottom"),
    (7, "Bottom"),
    (8, "Bottom"),
    (9, "Bottom"),
])
def test_layer_normalization(idx: int, expected_layer: str):
    result = _parse(LAYER_VARIANTS_CSV)
    assert result.entries[idx].layer == expected_layer


