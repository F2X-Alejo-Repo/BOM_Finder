def test_tab_delimiter():
    result = _parse(TAB_DELIMITED_CSV)
    assert len(result.entries) == 1
    assert result.entries[0].designator == "C1"


def test_semicolon_delimiter():
    result = _parse(SEMICOLON_DELIMITED_CSV)
